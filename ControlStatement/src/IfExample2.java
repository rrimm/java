
public class IfExample2 {

	public static void main(String[] args) {
		int num1 = 12;
		int num2 = 24;
		if (num1 < num2) {  // > 관계연산자는 항상 true 또는 false
			System.out.println("num1 = " + num1);
		}
		else {
			System.out.println("num2 = " + num2);
		}
		System.out.println("Done.");
//num1과 num2 중 큰 값을 출력합니다.
	}

}
