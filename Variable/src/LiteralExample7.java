
public class LiteralExample7 {

	public static void main(String[] args) {
		String str1 = "Hello, Java.\nNice to meet you."; //변수를 만들 때 앞글자가 대문자면 클래스라고 생각. string타입의 객체 생성. 사용자가 정할 수도, 자바가 임의로 정할 수도 있음
		String str2 = "나는 \"비틀즈는 1970년에 해체됐어요\"라고 말했다";
		System.out.println(str1);
		System.out.println(str2);

	}

}
