
public class AssignmentExample1 {

	public static void main(String[] args) {
		int num = 17;
		num += 1; // num = num + 1; 과 동일
		num -= 2; // num = num - 2; 과 동일
		num *= 3; // num = num * 3; 과 동일
		num /= 4; // num = num / 4; 과 동일
		num %= 5; // num = num % 5; 과 동일
		System.out.println(num);

	}

}
