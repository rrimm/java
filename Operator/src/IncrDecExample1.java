
public class IncrDecExample1 {

	public static void main(String[] args) {
		int num = 0;
		num++;  // num = num + 1
		System.out.println(num);
		++num;  // num = num + 1
		System.out.println(num);
		num--;  // num = num - 1
		System.out.println(num);
		--num;  // num = num - 1
		System.out.println(num);

	}

}
